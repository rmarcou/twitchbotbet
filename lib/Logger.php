<?php

namespace IZYBOT\lib;

class Logger {

    private $logger;

    public function __construct($logger_config) {

        $this->logger = new \Katzgrau\KLogger\Logger('log', \Psr\Log\LogLevel::DEBUG, array (
            'extension' => 'txt',
            'prefix' => $logger_config['log_file_prefix'] . '_',
        ));

    }
    //----------------------------------------------------------------------------------
    //
    //----------------------------------------------------------------------------------
    public function log_it($log_level, $caller_class, $caller_function, $message) {
        
        $log_level = strtolower($log_level);
        $message = $caller_class . '\\' . $caller_function . ': - ' . $message;
        //
        $this->logger->$log_level($message);
    }
    //----------------------------------------------------------------------------------
    //
    //----------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------
    //
    //----------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------
    //
    //----------------------------------------------------------------------------------

}
